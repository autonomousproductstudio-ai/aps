def _stack_str(trd) -> str:
    return " ".join(trd.stack).lower() if trd and getattr(trd, "stack", None) else ""


def _is_python(trd) -> bool:
    s = _stack_str(trd)
    return (not s) or any(k in s for k in ("fastapi", "python", "django", "flask"))


def _has_frontend(trd) -> bool:
    s = _stack_str(trd)
    return any(k in s for k in ("react", "next", "vue", "svelte", "typescript", "node", "frontend"))


def _db(trd) -> str:
    s = _stack_str(trd)
    for k in ("postgres", "mysql", "mongo", "sqlite", "redis"):
        if k in s:
            return "postgres" if k == "redis" else k
    return "postgres"


def _classname(name: str) -> str:
    parts = "".join(c if c.isalnum() else " " for c in (name or "Entity")).split()
    return "".join(p[:1].upper() + p[1:] for p in parts) or "Entity"


def _snake(name: str) -> str:
    out = "".join(("_" + c.lower()) if c.isupper() else c for c in (name or "")).strip("_")
    return "".join(c if c.isalnum() or c == "_" else "_" for c in out).strip("_").lower() or "item"


def _entities(trd) -> list[tuple[str, dict[str, str]]]:
    """Normalize TRD.data_model.entities (dict-of-dict or list-of-dict) → [(Name, {field: type})]."""
    dm = (getattr(trd, "data_model", None) or {}) if trd else {}
    ents = dm.get("entities") or {}
    out: list[tuple[str, dict[str, str]]] = []

    def _fields(spec: Any) -> dict[str, str]:
        f = (spec or {}).get("fields") if isinstance(spec, dict) else None
        f = f or {}
        if isinstance(f, list):
            return {d.get("name"): d.get("type", "string") for d in f if isinstance(d, dict) and d.get("name")}
        return {k: (v if isinstance(v, str) else "string") for k, v in f.items()} if isinstance(f, dict) else {}

    if isinstance(ents, dict):
        for name, spec in ents.items():
            out.append((str(name), _fields(spec)))
    elif isinstance(ents, list):
        for spec in ents:
            name = spec.get("name") if isinstance(spec, dict) else None
            if name:
                out.append((str(name), _fields(spec)))
    return out


def _paths(trd) -> dict:
    spec = (getattr(trd, "api_spec", None) or {}) if trd else {}
    return spec.get("paths") or {}


