def _arch_mermaid(trd) -> tuple[str, str]:
    try:
        from aps.render.architecture_mmd import _flowchart, _er
        dm = (getattr(trd, "data_model", None) or {}) if trd else {}
        return _flowchart(dm), _er(dm)
    except Exception:
        return "", ""


def _file_tree(paths: list[str]) -> str:
    """A simple indented tree from a flat list of file paths (for the README)."""
    tree: dict = {}
    for p in sorted(paths):
        node = tree
        for part in p.split("/"):
            node = node.setdefault(part, {})
    lines: list[str] = []

    def walk(node: dict, prefix: str) -> None:
        items = sorted(node.items())
        for i, (name, child) in enumerate(items):
            last = i == len(items) - 1
            lines.append(f"{prefix}{'└── ' if last else '├── '}{name}")
            if child:
                walk(child, prefix + ("    " if last else "│   "))
    walk(tree, "")
    return "```\n" + "\n".join(lines) + "\n```"


def _readme(idea: str, prd, trd, execution, file_paths: list[str]) -> str:
    flow, er = _arch_mermaid(trd)
    L: list[str] = []
    L.append(f"# {idea}\n")
    L.append("![status](https://img.shields.io/badge/status-scaffold-blue) "
             "![built with](https://img.shields.io/badge/built%20with-APS-7c3aed) "
             "![license](https://img.shields.io/badge/license-MIT-green)\n")
    L.append("> Generated by **Autonomous Product Studio** — idea → research → architecture → a runnable scaffold.\n")
    if prd and getattr(prd, "mvp_scope", ""):
        L.append("## Overview\n")
        L.append(prd.mvp_scope.strip() + "\n")
    if prd and getattr(prd, "features", None):
        L.append("## Features\n")
        for f in prd.features[:12]:
            L.append(f"- **{f.title}** `[{f.priority}]` — {f.description}")
        L.append("")
    if flow:
        L.append("## Architecture\n")
        L.append("```mermaid\n" + flow.strip() + "\n```\n")
    if er:
        L.append("## Data Model\n")
        L.append("```mermaid\n" + er.strip() + "\n```\n")
    if trd and getattr(trd, "stack", None):
        L.append("## Tech Stack\n")
        for s in trd.stack:
            L.append(f"- {s}")
        L.append("")
    L.append("## Project Structure\n")
    L.append(_file_tree(file_paths) + "\n")
    L.append("## Quick Start\n")
    if _is_python(trd):
        L.append("```bash")
        L.append("# 1) backend")
        L.append("cd backend && python -m venv .venv && source .venv/bin/activate")
        L.append("pip install -r requirements.txt")
        L.append("uvicorn app.main:app --reload      # → http://localhost:8000/docs")
        L.append("```")
    L.append("\nOr run the whole stack with Docker:\n")
    L.append("```bash\ndocker compose -f infra/docker-compose.yml up --build\n```\n")
    L.append("## Roadmap\n")
    L.append("See [`docs/ROADMAP.md`](docs/ROADMAP.md). Architecture details in "
             "[`docs/ARCHITECTURE.md`](docs/ARCHITECTURE.md); API in [`docs/API.md`](docs/API.md).\n")
    L.append("---\n*Scaffolded by APS. Each file has `TODO:` markers — implement them to ship.*\n")
    return "\n".join(L)


def _architecture_md(idea: str, trd) -> str:
    flow, er = _arch_mermaid(trd)
    L = [f"# Architecture — {idea}\n", _gen_note(), ""]
    L.append("## System Architecture\n")
    L.append("```mermaid\n" + (flow.strip() or "flowchart TD\n    Client --> API --> DB") + "\n```\n")
    L.append("## Data Model (ER)\n")
