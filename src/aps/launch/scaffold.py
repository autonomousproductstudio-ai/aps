"""Deterministic repo scaffold for GitHub Launch Mode.

Turns the artifacts APS has ALREADY generated (PRD, TRD, Execution plan) into a real starter
repository — README with rendered architecture + ER diagrams, models/routes derived from the
TRD data-model + OpenAPI spec, Docker/CI/config, and docs. Pure transformation: **no LLM and no
new API calls** (so it never re-burns research). Every file carries a header comment and `TODO:`
steps drawn from the backlog/features, so the repo reads like a proper scaffold, not an empty shell.

`build_scaffold(...)` returns `[(path, content)]`; the launcher commits them all in one tree.
"""
from __future__ import annotations

import json
from typing import Any

_GEN = "Generated by Autonomous Product Studio (APS) - fill in the TODOs to ship."

# data-model type → SQLAlchemy column type
_SA_TYPE = {
    "uuid": "UUID(as_uuid=True)", "string": "String(255)", "str": "String(255)", "text": "Text",
    "int": "Integer", "integer": "Integer", "bigint": "BigInteger", "float": "Float",
    "number": "Float", "decimal": "Float", "bool": "Boolean", "boolean": "Boolean",
    "datetime": "DateTime(timezone=True)", "timestamp": "DateTime(timezone=True)",
    "date": "Date", "json": "JSON", "jsonb": "JSON",
}


