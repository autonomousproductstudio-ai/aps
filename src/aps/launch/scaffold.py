def build_scaffold(idea: str, prd, trd, execution, pitch) -> list[tuple[str, str]]:
    """Build the full file list for the repo (paths relative to the repo root)."""
    db = _db(trd)
    files: list[tuple[str, str]] = []

    # docs + top-level (computed first so the README can show the real tree)
    docs: list[tuple[str, str]] = [
        ("docs/ARCHITECTURE.md", _architecture_md(idea, trd)),
        ("docs/ROADMAP.md", _roadmap_md(execution)),
        ("CONTRIBUTING.md", _contributing(idea)),
        ("LICENSE", _license()),
        (".gitignore", _gitignore()),
        (".env.example", _env_example(db)),
        (".github/workflows/ci.yml", _ci()),
    ]
    if trd and getattr(trd, "api_spec", None):
        docs.append(("docs/API.md", _api_md(trd)))
        docs.append(("docs/openapi.json", json.dumps(trd.api_spec, indent=2) + "\n"))

    backend: list[tuple[str, str]] = []
    if _is_python(trd):
        backend = [
            ("backend/app/__init__.py", ""),
            ("backend/app/main.py", _main_py(idea)),
            ("backend/app/config.py", _config_py(idea, db)),
            ("backend/app/api/__init__.py", ""),
            ("backend/app/api/routes.py", _routes_py(trd)),
            ("backend/app/models/__init__.py", ""),
            ("backend/app/models/schema.py", _models_py(trd)),
            ("backend/requirements.txt", _requirements(db)),
            ("backend/Dockerfile", _dockerfile()),
            ("backend/tests/test_smoke.py", _test_smoke(idea)),
        ]

    frontend: list[tuple[str, str]] = []
    if _has_frontend(trd):
        frontend = [
            ("frontend/package.json", _package_json(idea)),
            ("frontend/README.md", f"# {idea} — Frontend\n\n{_gen_note()}\n\n`npm install && npm run dev`\n"),
        ]

    infra = [("infra/docker-compose.yml", _compose(idea, db))]

    body_files = docs + backend + frontend + infra
    all_paths = [p for p, _ in body_files] + ["README.md"]
    readme = _readme(idea, prd, trd, execution, all_paths)

    files = [("README.md", readme)] + body_files
    return files
