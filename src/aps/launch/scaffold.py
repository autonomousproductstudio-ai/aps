def _api_md(trd) -> str:
    paths = _paths(trd)
    L = ["# API Reference\n", _gen_note(), "", "Derived from `docs/openapi.json`.\n", "| Method | Path | Summary |", "|---|---|---|"]
    for path, methods in paths.items():
        for method, op in (methods or {}).items():
            if method.lower() not in ("get", "post", "put", "patch", "delete"):
                continue
            summary = (op or {}).get("summary") or (op or {}).get("operationId") or ""
            L.append(f"| `{method.upper()}` | `{path}` | {summary} |")
    if len(L) == 6:
        L.append("| `GET` | `/health` | Liveness probe |")
    return "\n".join(L) + "\n"


def _roadmap_md(execution) -> str:
    L = ["# Roadmap\n", _gen_note(), ""]
    if execution and getattr(execution, "roadmap", ""):
        L.append(execution.roadmap.strip() + "\n")
    for s in (getattr(execution, "sprints", None) or []):
        L.append(f"## Sprint {s.get('sprint', '?')}\n")
        for it in s.get("items", []):
            L.append(f"- [ ] {it.get('title', 'task')}  `{it.get('priority', '')}`")
        L.append("")
    return "\n".join(L)


def _models_py(trd) -> str:
    ents = _entities(trd)
    head = [
        '"""Database models — generated from the APS Technical Design data model.',
        "",
        _GEN,
        '"""',
        "from __future__ import annotations",
        "",
        "import uuid",
        "",
        "from sqlalchemy import (Column, String, Integer, BigInteger, Float, Boolean,",
        "                        DateTime, Date, Text, JSON, ForeignKey)  # noqa: F401",
        "from sqlalchemy.dialects.postgresql import UUID",
        "from sqlalchemy.orm import declarative_base",
        "",
        "Base = declarative_base()",
        "",
        "",
    ]
    body: list[str] = []
    if not ents:
        body += ["# TODO: the data model had no entities — define your tables here.", ""]
    for name, fields in ents:
        cls = _classname(name)
        body.append(f"class {cls}(Base):")
        body.append(f'    """{name}."""')
        body.append(f'    __tablename__ = "{_snake(name)}s"')
        if "id" not in fields:
            body.append("    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)")
        for fname, ftype in fields.items():
            sa = _SA_TYPE.get((ftype or "string").strip().lower(), "String(255)")
            args = [sa]
            if fname == "id":
                args.append("primary_key=True")
                if "uuid" in sa.lower():
                    args.append("default=uuid.uuid4")
            note = "  # TODO: foreign key — point to the referenced table" if fname.endswith("_id") and fname != "id" else ""
            body.append(f"    {fname} = Column({', '.join(args)}){note}")
        body += ["", ""]
    return "\n".join(head + body)


def _routes_py(trd) -> str:
    paths = _paths(trd)
    head = [
        '"""API routes — generated from the APS OpenAPI design (docs/openapi.json).',
        "",
        "Each endpoint's method + path + params are real; the body is a TODO stub.",
        _GEN,
        '"""',
        "from __future__ import annotations",
        "",
        "from fastapi import APIRouter, HTTPException",
        "",
        'router = APIRouter()',
        "",
        "",
    ]
    body: list[str] = []
    seen: set[str] = set()
    for path, methods in paths.items():
        for method, op in (methods or {}).items():
            m = method.lower()
            if m not in ("get", "post", "put", "patch", "delete"):
                continue
            summary = (op or {}).get("summary") or (op or {}).get("operationId") or f"{m.upper()} {path}"
            params = [seg[1:-1] for seg in path.split("/") if seg.startswith("{") and seg.endswith("}")]
            fn = (m + "_" + "_".join(p for p in path.strip("/").split("/") if not p.startswith("{"))).strip("_") or f"{m}_root"
            while fn in seen:
                fn += "_x"
            seen.add(fn)
            sig = ", ".join(f"{p}: str" for p in params)
            body.append(f'@router.{m}("{path}")')
            body.append(f"def {fn}({sig}):")
            body.append(f'    """{summary}."""')
            body.append(f"    # TODO: implement {m.upper()} {path}")
            body.append('    raise HTTPException(status_code=501, detail="Not implemented yet")')
            body.append("")
    if not body:
        body = ['@router.get("/health")', "def health():", '    """Liveness probe."""',
                '    return {"status": "ok"}', ""]
    return "\n".join(head + body)


def _main_py(idea: str) -> str:
    return (
        f'"""FastAPI application entry point for {idea}.\n\n{_GEN}\n"""\n'
        "from fastapi import FastAPI\n\n"
        "from app.api.routes import router\n\n"
        f'app = FastAPI(title="{idea}", version="0.1.0")\n'
        "app.include_router(router)\n\n\n"
        '@app.get("/")\n'
        "def root():\n"
        f'    return {{"app": "{idea}", "status": "scaffolded by APS"}}\n'
    )


def _config_py(idea: str, db: str) -> str:
    dsn = {"postgres": "postgresql+psycopg://app:app@localhost:5432/app",
           "mysql": "mysql+pymysql://app:app@localhost:3306/app",
           "sqlite": "sqlite:///./app.db", "mongo": "mongodb://localhost:27017/app"}.get(db, "sqlite:///./app.db")
    return (
        f'"""App configuration. {_GEN}"""\n'
        "import os\n\n\n"
        "class Settings:\n"
        f'    app_name: str = "{idea}"\n'
        f'    database_url: str = os.getenv("DATABASE_URL", "{dsn}")\n'
        '    debug: bool = os.getenv("DEBUG", "true").lower() == "true"\n\n\n'
        "settings = Settings()\n"
    )


def _requirements(db: str) -> str:
    reqs = ["fastapi>=0.111", "uvicorn[standard]>=0.30", "pydantic>=2.7", "SQLAlchemy>=2.0"]
    if db == "postgres":
        reqs.append("psycopg[binary]>=3.1")
    elif db == "mysql":
        reqs.append("PyMySQL>=1.1")
    reqs.append("python-dotenv>=1.0")
    return "# " + _GEN + "\n" + "\n".join(reqs) + "\n"


def _dockerfile() -> str:
    return (
        "# " + _GEN + "\n"
        "FROM python:3.11-slim\n"
        "WORKDIR /app\n"
        "COPY requirements.txt .\n"
        "RUN pip install --no-cache-dir -r requirements.txt\n"
        "COPY . .\n"
        'CMD ["uvicorn", "app.main:app", "--host", "0.0.0.0", "--port", "8000"]\n'
    )


